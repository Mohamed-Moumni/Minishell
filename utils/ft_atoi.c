#include "../headers/struct.h"
#include "../headers/minishell.h"
#include "../headers/builtins.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			sign;
	int			result;

	i = 0;
	result = 0;
	sign = 1;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	return ((sign * result));
}
